
let data = [1, 2, 3 , 4];

console.log(data);

// push ==> add element in last 
data.push(8);
console.log(data);

// pop ==> remove element from last
data.pop();
console.log(data);


// shift data
console.log("shift data", data.shift());
console.log(data);


// un-shift data
console.log("unshift data", data.unshift(1 ));
console.log(data);